'use strict';


// Declare variables for first and last name
let first = 'Pieter';
let last = 'Rabbit';
// Concatenate first and last name to create full name
let fullName = first + ' ' + last;

// log the full name to the console
console.log(fullName);

// create a greeting 'hello' with the full name
let greeting = 'hello ' + fullName;

// change the greeting to 'good bye' with the full name
greeting = 'good bye ' + fullName;

// log the updated greeting to the console
console.log(greeting);
